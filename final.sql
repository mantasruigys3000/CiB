CREATE TABLE IF NOT EXISTS "ROLES" (
	"bit_value"	INTEGER NOT NULL,
	"name"	INTEGER NOT NULL UNIQUE,
	PRIMARY KEY("bit_value")
);
CREATE TABLE IF NOT EXISTS "DEPARTMENT_PARKING" (
	"department_number"	INTEGER NOT NULL,
	"total_spaces"	INTEGER NOT NULL,
	"disabled"	INTEGER NOT NULL,
	FOREIGN KEY("department_number") REFERENCES "DEPARTMENT"("id"),
	PRIMARY KEY("department_number")
);
CREATE TABLE IF NOT EXISTS "EMPLOYEE_TIMETABLE" (
	"employee_id"	INTEGER NOT NULL,
	"datetime_start"	TEXT NOT NULL,
	"datetime_end"	TEXT NOT NULL,
	"vehicle_registration"	TEXT NOT NULL,
	FOREIGN KEY("vehicle_registration") REFERENCES "VEHICLE"("registration"),
	FOREIGN KEY("employee_id") REFERENCES "EMPLOYEE"("id")
);
CREATE TABLE IF NOT EXISTS "VEHICLE" (
	"employee_id"	INTEGER NOT NULL,
	"registration"	TEXT NOT NULL UNIQUE,
	"make"	TEXT NOT NULL,
	"electric_vehicle"	INTEGER NOT NULL,
	PRIMARY KEY("employee_id","registration"),
	FOREIGN KEY("employee_id") REFERENCES "EMPLOYEE"("id")
);
CREATE TABLE IF NOT EXISTS "DEPARTMENT" (
	"id"	INTEGER NOT NULL,
	"manager_id"	INTEGER NOT NULL,
	"department_name"	TEXT NOT NULL,
	PRIMARY KEY("id")
);
CREATE TABLE IF NOT EXISTS "SESSION" (
	"user_id"	INTEGER,
	"token"	TEXT,
	"session_expiry"	TEXT,
	"ip_address"	TEXT,
	FOREIGN KEY("user_id") REFERENCES "EMPLOYEE"("id")
);
